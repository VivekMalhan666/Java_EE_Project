package com.savelife.models;

public class Sponsor {
	private String childId;
	private String userEmail;
	private String sponsorType;
	private int amount;
	private int chequeDdNumber;
	private String bankName;
	private String branchName;
	public String getChildId() {
		return childId;
	}
	public void setChildId(String childId) {
		this.childId = childId;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getSponsorType() {
		return sponsorType;
	}
	public void setSponsorType(String sponsorType) {
		this.sponsorType = sponsorType;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int s4) {
		this.amount = s4;
	}
	public int getChequeDdNumber() {
		return chequeDdNumber;
	}
	public void setChequeDdNumber(int chequeDdNumber) {
		this.chequeDdNumber = chequeDdNumber;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	
	
	
	
	
	
	
	
	
}
